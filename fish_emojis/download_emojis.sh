# Twemoji CDN base URL (72x72 transparent PNGs)
TWEMOJI="https://cdn.jsdelivr.net/gh/twitter/twemoji@14.0.2/assets/72x72"

# Function to download from Twemoji
dl_twemoji() {
    local name="$1"
    local codepoint="$2"
    echo "Downloading $name..."
    curl -sL "$TWEMOJI/$codepoint.png" -o "${name}.png"
}

# Function to download Discord custom emoji
dl_discord() {
    local name="$1"
    local id="$2"
    local animated="$3"
    echo "Downloading $name (Discord)..."
    if [ "$animated" = "true" ]; then
        curl -sL "https://cdn.discordapp.com/emojis/$id.gif?v=1" -o "${name}.gif"
    else
        curl -sL "https://cdn.discordapp.com/emojis/$id.png?v=1" -o "${name}.png"
    fi
}

